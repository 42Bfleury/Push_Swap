#include "../includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	c %= 256;
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
