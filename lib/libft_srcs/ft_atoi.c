#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	result;
	int	negative;

	i = 0;
	result = 0;
	negative = 0;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			negative = 1;
	while (ft_isdigit(str[i]))
		result = (result * 10) + (str[i++] - '0');
	if (negative)
		return (-result);
	return (result);
}
